esp_err_t WiFiManager::ipEventHandler(void *ctx, system_event_t *event){
    
    if(event->event_id == SYSTEM_EVENT_STA_GOT_IP)
        xTaskCreate(UpdateMonitorTask, "updateMonitorTask", 4096, NULL, 1, NULL);

    return ESP_OK;
}

void WiFiManager::WiFiStationConnected(WiFiEvent_t event, WiFiEventInfo_t info)
{
    Serial.println("Connected to AP!");
    xTaskCreate(UpdateMonitorTask, "updateMonitorTask", 5120, NULL, 1, NULL);
}

